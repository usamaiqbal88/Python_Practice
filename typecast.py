# Typecasting


# Integers:

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("36") # z will be 36

print(type(x))
print(x,y,z)

#Floats:

a = float(1)     # a will be 1.0
b = float(2.8)   # s will be 2.8
c = float("3")   # c will be 3.0
d = float("4.2") # d will be 4.2

print(type(a))
print(a,b,c,d)

#Strings:

j = str("s1") # j will be 's1'
k = str(2)    # k will be '2'
l = str(3.0)  # l will be '3.0'

print(type(j))
print(j,k,l)

print( j+k)